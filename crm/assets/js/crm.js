/* ==========================================================================
   Better Tap — Operations Console  (crm.js)
   --------------------------------------------------------------------------
   PROTOTYPE / DEMO ONLY — front-end only. No real backend.

   What a PRODUCTION build would need (NOT implemented here):
     • Real backend / API server (data fetched & saved via authenticated HTTPS)
     • Real database (Postgres/etc.), encrypted at rest, with backups
     • Hashed + salted passwords (bcrypt/argon2) — never plaintext, never client-checked
     • Real two-factor auth (TOTP/WebAuthn) verified server-side, not "any 6 digits"
     • Server-issued session tokens / secure HttpOnly cookies — not a localStorage flag
     • RBAC (role-based access control) + per-record permission checks
     • Audit logs for sensitive actions; rate limiting; CSRF protection
     • Input validation & sanitization on the server (never trust the client)
   Here, auth/2FA are simulated and ALL data is persisted in localStorage so the
   demo survives reloads.

   ADDITIONAL PRODUCTION NEEDS for the features added in this build:
     • Multi-agent identity: real user accounts/SSO; the "agent" derived from an
       email here would be an authenticated user record server-side.
     • Start-of-shift check-in: stored as a timestamped attendance/shift record.
     • Omnichannel comms (Call / SMS / WhatsApp / Email / Messenger): real
       integrations — Twilio (voice/SMS), WhatsApp Business API, an email/SMTP
       provider, and the Meta Messenger Platform — with webhooks writing inbound
       messages back into a per-customer conversation store.
     • AI call transcripts: produced by a real speech-to-text + LLM pipeline on
       recorded calls (with consent/recording disclosure & retention policy).
     • AI status summaries / next-best-action: generated by an LLM over the
       customer's actual history. Here they are HARD-CODED SAMPLE TEXT (simulated).
     • Lead attribution: real UTM/click-id capture, call tracking numbers, and
       form/source tagging persisted per lead; the numbers shown are demo data.
   ========================================================================== */

